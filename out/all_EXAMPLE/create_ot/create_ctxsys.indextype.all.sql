CREATE OR REPLACE INDEXTYPE "CTXSYS"."CONTEXT" FOR
	 "CTXSYS"."CONTAINS" (VARCHAR2, VARCHAR2),
	 "CTXSYS"."CONTAINS" (VARCHAR2, CLOB),
	 "CTXSYS"."CONTAINS" (CLOB, VARCHAR2),
	 "CTXSYS"."CONTAINS" (CLOB, CLOB),
	 "CTXSYS"."CONTAINS" (BLOB, VARCHAR2),
	 "CTXSYS"."CONTAINS" (BLOB, CLOB),
	 "CTXSYS"."CONTAINS" (BFILE, VARCHAR2),
	 "CTXSYS"."CONTAINS" (BFILE, CLOB),
	 "CTXSYS"."CONTAINS" ("SYS"."XMLTYPE", VARCHAR2),
	 "CTXSYS"."CONTAINS" ("SYS"."XMLTYPE", CLOB),
	 "CTXSYS"."CONTAINS" ("SYS"."URITYPE", VARCHAR2),
	 "CTXSYS"."CONTAINS" ("SYS"."URITYPE", CLOB)
	 USING "CTXSYS"."TEXTINDEXMETHODS"
	 WITHOUT COLUMN DATA
	 WITH ARRAY DML
	 WITH REBUILD ONLINE
