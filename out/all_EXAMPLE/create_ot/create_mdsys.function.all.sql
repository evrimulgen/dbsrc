    RETURN TREAT(g AS ST_Curve).ST_IsClosed();
  END IF;
  IF(nls_upper(OGC_GeometryType(g)) IN ('MULTILINESTRING', 'ST_MULTILINESTRING', 'ST_MULTICURVE')) THEN
    RETURN TREAT(g AS ST_MultiCurve).ST_IsClosed();
  END IF;
  RETURN NULL;
END OGC_IsClosed;
